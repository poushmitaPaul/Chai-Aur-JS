// ++++++++++++++++++ SWITCH-STATEMENT  ++++++++++++++++++ //

const month = 3

switch(month){
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march");
        break;        
    case 4:
        console.log("april");
        break;    
    case 5:
        console.log("may");
        break;  
    case 6:
        console.log("june");
        break;  
    case 7:
        console.log("july");
        break;  
    case 8:
        console.log("august");
        break;  
    case 9:
        console.log("september");
        break;  
    case 10:
        console.log("october");
        break;
    default:
        console.log("enter proper number");
        break;  
}